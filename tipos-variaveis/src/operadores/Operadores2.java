package operadores;

public class Operadores2 {
    public static void main(String[] args) {

        int num = 10;
        // 10 vai receber mais 1 com ++ ou menos 1 com --
        System.out.println(num++);
        System.out.println(num);

        // booleans exemplo
        boolean variavel = false;
        System.out.println(variavel);
        //

        // negação ! uasndo o exemplo do boolean de falso
        System.out.println(!variavel); // virou true. ou seja valor invertido
        //
    }
}
